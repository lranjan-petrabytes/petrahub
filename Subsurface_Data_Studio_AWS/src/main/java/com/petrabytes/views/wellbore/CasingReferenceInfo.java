package com.petrabytes.views.wellbore;

public class CasingReferenceInfo {
	String casing_id;
	String casing_od;
	String drift;
	String max_od_liner;
	String wall;
	String weight;
	public String getCasing_id() {
		return casing_id;
	}
	public void setCasing_id(String casing_id) {
		this.casing_id = casing_id;
	}
	public String getCasing_od() {
		return casing_od;
	}
	public void setCasing_od(String casing_od) {
		this.casing_od = casing_od;
	}
	public String getDrift() {
		return drift;
	}
	public void setDrift(String drift) {
		this.drift = drift;
	}
	public String getMax_od_liner() {
		return max_od_liner;
	}
	public void setMax_od_liner(String max_od_liner) {
		this.max_od_liner = max_od_liner;
	}
	public String getWall() {
		return wall;
	}
	public void setWall(String wall) {
		this.wall = wall;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	

}
