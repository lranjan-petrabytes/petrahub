package com.petrabytes.views.well_Information;

public class Basin_Registry_Info {


	private long basinID;
	private String basinName;

	private String basinid;

	/**
	 */
	public Basin_Registry_Info() {
		super();
		this.basinID = basinID;
		this.basinName = basinName;
		this.basinid = basinid;
	}

	public long getBasinID() {
		return basinID;
	}

	public void setBasinID(long basinID) {
		this.basinID = basinID;
	}

	public String getBasinName() {
		return basinName;
	}

	public void setBasinName(String basinName) {
		this.basinName = basinName;
	}

	public String getBasinid() {
		return basinid;
	}

	public void setBasinid(String basinid) {
		this.basinid = basinid;
	}
}