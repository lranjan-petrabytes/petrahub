package com.petrabytes.views.osdu;

public class OSDUWellboreTrajectoryInfo {
	
	private String name = null;
	private String dataset = null;
	private Double topMD = null;
	private Double bottomMD = null;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDataset() {
		return dataset;
	}
	public void setDataset(String dataset) {
		this.dataset = dataset;
	}
	public Double getTopMD() {
		return topMD;
	}
	public void setTopMD(Double topMD) {
		this.topMD = topMD;
	}
	public Double getBottomMD() {
		return bottomMD;
	}
	public void setBottomMD(Double bottomMD) {
		this.bottomMD = bottomMD;
	}

}
